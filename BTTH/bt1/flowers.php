// Hàm lấy tất cả các loài hoa
function getAllFlowers() {
    global $flowers;
    return $flowers;
}

// Hàm lấy thông tin một loài hoa theo ID
function getFlowerById($id) {
    global $flowers;
    foreach ($flowers as $flower) {
        if ($flower['id'] == $id) {
            return $flower;
        }
    }
    return null;
}

// Hàm thêm hoa mới (giả lập - trong thực tế cần database)
function addFlower($name, $description, $image) {
    global $flowers;
    $newId = count($flowers) > 0 ? max(array_column($flowers, 'id')) + 1 : 1;
    $newFlower = [
        'id' => $newId,
        'name' => $name,
        'description' => $description,
        'image' => $image
    ];
    $flowers[] = $newFlower;
    return $newFlower;
}

// Hàm cập nhật thông tin hoa (giả lập)
function updateFlower($id, $name, $description, $image) {
    global $flowers;
    foreach ($flowers as $key => $flower) {
        if ($flower['id'] == $id) {
            $flowers[$key]['name'] = $name;
            $flowers[$key]['description'] = $description;
            $flowers[$key]['image'] = $image;
            return true;
        }
    }
    return false;
}

// Hàm xóa hoa (giả lập)
function deleteFlower($id) {
    global $flowers;
    foreach ($flowers as $key => $flower) {
        if ($flower['id'] == $id) {
            unset($flowers[$key]);
            $flowers = array_values($flowers); // Re-index array
            return true;
        }
    }
    return false;
}
